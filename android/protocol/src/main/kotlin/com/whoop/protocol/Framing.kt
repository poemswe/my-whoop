fun crc8(bytes: UByteArray): UByte {
    var crc: UByte = 0u
    for (b in bytes) {
        crc = CRC8_TABLE[(crc xor b).toInt()]
    }
    return crc
}

private val CRC32_TABLE: UIntArray = UIntArray(256) { i ->
    var c = i.toUInt()
    repeat(8) { c = if ((c and 1u) != 0u) 0xEDB88320u xor (c shr 1) else c shr 1 }
    c
}

fun crc32(bytes: UByteArray): UInt {
    var crc: UInt = 0xFFFFFFFFu
    for (b in bytes) {
        crc = CRC32_TABLE[((crc xor b.toUInt()) and 0xFFu).toInt()] xor (crc shr 8)
    }
    return crc xor 0xFFFFFFFFu
}

data class FrameCheck(
    val ok: Boolean,
    val length: Int? = null,
    val crc8OK: Boolean? = null,
    val crc32OK: Boolean? = null,
)

private fun u16le(b: UByteArray, off: Int): Int =
    b[off].toInt() or (b[off + 1].toInt() shl 8)

private fun u32le(b: UByteArray, off: Int): UInt =
    b[off].toUInt() or (b[off + 1].toUInt() shl 8) or
        (b[off + 2].toUInt() shl 16) or (b[off + 3].toUInt() shl 24)

fun verifyFrame(frame: UByteArray): FrameCheck {
    if (frame.size < 8 || frame[0] != 0xAAu.toUByte()) return FrameCheck(ok = false)
    val length = u16le(frame, 1)
    val crc8OK = crc8(ubyteArrayOf(frame[1], frame[2])) == frame[3]
    var crc32OK: Boolean? = null
    if (length >= 7 && length + 4 <= frame.size) {
        val inner = frame.sliceArray(4 until length)
        crc32OK = crc32(inner) == u32le(frame, length)
    }
    return FrameCheck(ok = crc8OK && crc32OK == true, length = length, crc8OK = crc8OK, crc32OK = crc32OK)
}

fun frameFromPayload(
    data: UByteArray,
    type: UByte,
    seq: UByte = 0u,
    cmd: UByte = 0u,
): UByteArray {
    val inner = ubyteArrayOf(type, seq, cmd) + data
    val length = inner.size + 4
    val c = crc32(inner)
    val out = UByteArray(4 + inner.size + 4)
    out[0] = 0xAAu
    out[1] = (length and 0xFF).toUByte()
    out[2] = ((length shr 8) and 0xFF).toUByte()
    out[3] = 0x00u
    inner.copyInto(out, destinationOffset = 4)
    val crcOff = 4 + inner.size
    out[crcOff]     = (c and 0xFFu).toUByte()
    out[crcOff + 1] = ((c shr 8) and 0xFFu).toUByte()
    out[crcOff + 2] = ((c shr 16) and 0xFFu).toUByte()
    out[crcOff + 3] = ((c shr 24) and 0xFFu).toUByte()
    return out
}

class Reassembler {
    private var buf: UByteArray = UByteArray(0)

    fun feed(fragment: UByteArray): List<UByteArray> {
        buf += fragment
        val out = mutableListOf<UByteArray>()
        while (true) {
            val sof = buf.indexOf(0xAAu.toUByte())
            if (sof < 0) {
                buf = UByteArray(0)
                break
            }
            if (sof > 0) buf = buf.sliceArray(sof until buf.size)
            if (buf.size < 4) break
            val total = (buf[1].toInt() or (buf[2].toInt() shl 8)) + 4
            if (buf.size < total) break
            out.add(buf.sliceArray(0 until total))
            buf = buf.sliceArray(total until buf.size)
        }
        return out
    }
}
